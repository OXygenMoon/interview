def add_general_company():
    """添加通用公司及其岗位"""
    with app.app_context():
        try:
            # 检查是否已存在同名公司
            existing = Company.query.filter_by(name=GENERAL_COMPANY['name']).first()
            if existing:
                print(f"⚠️ 公司 '{GENERAL_COMPANY['name']}' 已存在，跳过创建")
                company = existing
            else:
                # 创建新公司
                company = Company(
                    name=GENERAL_COMPANY['name'],
                    description=GENERAL_COMPANY['description']
                )
                db.session.add(company)
                db.session.flush()  # 获取ID
                print(f"✅ 创建公司: {company.name}")
            
            # 添加岗位
            added_count = 0
            for pos_data in SOCIAL_POSITIONS:
                # 检查岗位是否已存在
                existing_pos = Position.query.filter_by(
                    name=pos_data['name'], 
                    company_id=company.id
                ).first()
                
                if existing_pos:
                    print(f"⚠️ 岗位 '{pos_data['name']}' 已存在，跳过")
                    continue
                
                # 创建新岗位
                position = Position(
                    company_id=company.id,
                    name=pos_data['name'],
                    description=pos_data['description']
                )
                db.session.add(position)
                added_count += 1
                print(f"✅ 添加岗位: {position.name}")
            
            # 提交事务
            db.session.commit()
            print(f"\n🎉 操作完成！")
            print(f"公司: {company.name}")
            print(f"新增岗位: {added_count} 个")
            print(f"总岗位数: {len(company.positions)} 个")
            
        except Exception as e:
            db.session.rollback()
            print(f"❌ 操作失败: {e}")
            raise

if __name__ == "__main__":
    add_general_company()